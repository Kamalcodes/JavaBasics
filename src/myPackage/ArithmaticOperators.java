package myPackage;

public class ArithmaticOperators
{
	
	public static void main(String[] args) 
	{		 
		 int num1 = 100;
		 int num2 = 50;
		 System.out.println("Addition of two numbers is  "+(num1+num2));
		 System.out.println("Addition of two numbers is  "+(num1-num2));
		 System.out.println("Addition of two numbers is  "+(num1*num2));
		 System.out.println("Addition of two numbers is  "+(num1/num2));
		 System.out.println("Addition of two numbers is  "+(num1%num2));
	}
	

	
	
	
	
}