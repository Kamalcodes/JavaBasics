
public final class FinalConcept 
{
	//to prevent method overriding. we use Final.
	//difining a final keyword in parent class will not allow to 
	//create the child class.
	//declaring the class as a final will prevent the method overriding.
	//we can also declare the final keyword in method.
	
	public static void main(String args[])
	{
		final int i=10;
		
		System.out.println(i);
		
	}
}


